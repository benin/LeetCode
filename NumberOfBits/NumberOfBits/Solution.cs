        public int HammingWeight(uint n) 
        {
            return
                _bitMap[n & 0x000000FF] +
                _bitMap[(n & 0x0000FF00) >> 8] +
                _bitMap[(n & 0x00FF0000) >> 16] +
                _bitMap[(n & 0xFF000000) >> 24];
        }
    }
